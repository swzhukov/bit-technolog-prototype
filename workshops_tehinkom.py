def format_workshops_for_prompt() -> str:
    """Возвращает текстовый блок для system prompt."""
    lines = ["# РЕАЛЬНЫЕ УЧАСТКИ И ОПЕРАЦИИ ТЕХИНКОМ-ЦЕНТР",
             "",
             "При генерации маршрута используй ТОЛЬКО эти участки и операции.",
             "НЕ придумывай операции, которых нет в этом списке.",
             ""]
    for section, workshops in TECHINKOM_WORKSHOPS.items():
        lines.append(f"\n## {section}")
        for ws, ops in workshops.items():
            lines.append(f"\n### {ws}")
            for op in ops:
                lines.append(f"- {op}")
    return "\n".join(lines)


TECHINKOM_WORKSHOPS_CONTEXT = format_workshops_for_prompt()
